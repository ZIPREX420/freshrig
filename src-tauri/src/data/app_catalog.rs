fn app(
    id: &str,
    name: &str,
    desc: &str,
    category: AppCategory,
    icon: &str,
    popular: bool,
) -> AppEntry {
    let estimated_size_mb = estimate_size(id);
    let tier = tier_for(id);
    AppEntry {
        id: id.to_string(),
        name: name.to_string(),
        description: desc.to_string(),
        category,
        icon_name: icon.to_string(),
        is_popular: popular,
        tier,
        estimated_size_mb,
    }
}

